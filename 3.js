// 연산자
var a = 1;
var b = 2;
console.log(a / b); // 몫
console.log(a % b); // 나머지

// 연결 연산자
var a = "1";
var b = "2";
console.log(a + b);

// 복합 연산자
var a = 5;
a += 5;
console.log(a);

// 증감 연산자
var a = 10;
a++;
console.log(a); 
// a++이면 +가 된 결과가 나오지 않음(뒤에 붙히면 해당 라인 다음에 값이 바뀜)
// ++a는 바로 값이 바뀜

// 논리 연산자
